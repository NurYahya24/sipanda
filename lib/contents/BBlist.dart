      B = 'K';
    }else{
      B = 'B';
    }
  }
  //logics of A
  if(male){
    if(prevNull){
      A = 'O';
    }else{
      if((bb - bbPrev) >= _kbmMale[bulan]){
        A = 'N';
      }else{
        A = 'T';
      }
    }
  }else{
    if(prevNull){
      A = 'O';
    }else{
      if((bb - bbPrev) >= _kbmFemale[bulan]){
        A = 'N';
      }else{
        A = 'T';
      }
    }
  }
  return '$A/$B';
}